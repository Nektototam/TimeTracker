-- Настройка для автоматической очистки старых записей
-- Этот скрипт нужно выполнить в SQL-консоли с правами администратора

-- Включаем расширение для работы с cron (если не включено)
CREATE EXTENSION IF NOT EXISTS pg_cron;

-- Создаем задачу, которая будет выполняться раз в день в 3:00 утра
SELECT cron.schedule('daily-cleanup', '0 3 * * *', 'SELECT clean_old_time_entries()');

-- Примечание: Для использования pg_cron требуются расширенные права администратора
-- и включение этой функциональности на уровне базы данных.
-- Если вы используете Supabase, вам может потребоваться связаться с их поддержкой
-- или использовать другой способ автоматизации (например, внешний cron-job). 